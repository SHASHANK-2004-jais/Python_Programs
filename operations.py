
a = 10
b = 5

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)


print("\nComparison Operations")
print("a > b:", a > b)
print("a < b:", a < b)
print("a == b:", a == b)
print("a != b:", a != b)


print("\nLogical Operations")
print("(a > 0 and b > 0):", a > 0 and b > 0)
print("(a > 0 or b < 0):", a > 0 or b < 0)
print("not(a > b):", not(a > b))